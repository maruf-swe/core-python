i = 1
while i < 6:
  print(i)
  i += 1

# break statement
i =0
while i < 6:
    if i == 2:
        break
    print(i)
    i +=1

# continue statement
i =0
while i < 6:
    if i == 2:
        continue
    print(i)
    i +=1

# else statement
print("else statement")
i = 1
while i < 6:
  print(i)
  i += 1
else:
  print("i is no longer less than 6")

